import math
x=3.5
y=3.9
z=3.3
print(int(x),int(y),int(z))
print(round(x),round(y),round(z))
print(math.ceil(x),math.ceil(y),math.ceil(z))#ciel redondea el numero al mayor al techo. Ejemplo:2.7 pasa a ser 3 
print(math.floor(x),math.floor(y),math.floor(z))#floor redondea el numero al piso. Ejemplo:2.7 pasa a ser 2